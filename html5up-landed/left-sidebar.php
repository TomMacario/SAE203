					<h3>À quoi ressemble une arnaque au faux crédit ?</h3>
					<p>Très présente sur les réseaux sociaux, l’arnaque au crédit fleurit aussi sur les sites web, et même dans les commentaires de certains articles ou forums. Le principe ? Sous couvert de rendre service, un escroc propose des crédits personnels aux ménages qui ne parviennent pas à en obtenir un auprès d’une banque, le tout à des taux défiant généralement toute concurrence. Les futures victimes sont ainsi invitées à envoyer une demande de prêt à une adresse mail. Les échanges avec la personne mal intentionnée peuvent alors mener à une demande de paiement pour prouver sa bonne foi ou au titre de frais de dossier, mais aussi sur une usurpation de documents personnels.

						Sur les six pistes suivies par MoneyVox dans son enquête, toutes ont ainsi abouti à une demande d’envoi de documents d’identité, de justificatif de domicile, de bulletins de salaire et de RIB. Selon Thierry Pezennec, chef du Service d’information, de renseignement et d’analyse stratégique sur la criminalité organisée (Sirasco), l’objectif des escrocs est clair : récupérer les pièces nécessaires pour ouvrir un compte courant de façon frauduleuse et y blanchir de l’argent. Thierry Pezennec déclare ainsi : « Leur objectif est de créer des myriades de comptes bancaires, partout en Europe, prêts à recevoir des fonds résultant d’escroqueries et de fraudes. Des comptes souvent dormants, qui peuvent être activés à tout moment ».
					</p>

					<h3>Qui sont victimes favorites des escrocs ?</h3>
					<p>Pour mettre toutes les chances de leur côté, les escrocs ciblent les personnes qui ont des difficultés à accéder au crédit via les établissements bancaires. Cela peut être le cas des ménages qui ont de faibles revenus, qui sont en CDD, en intérim, à la retraite, au chômage ou au RSA, ou encore ceux qui éprouvent des difficultés de paiement et qui sont notamment fichés au FCC (Fichier central des chèques) ou au FICP (Fichier des incidents de remboursement des crédits aux particuliers). Dans l’impasse pour trouver un financement pour remplacer un appareil électroménager tombé en panne, pour réparer un véhicule ou même pour devenir propriétaires, certains sont alors tentés par ces solutions qui semblent simples, accessibles et peu coûteuses.<br>
						Il faut dire que les annonces qui circulent sur les sites et les réseaux sociaux sont alléchantes, avec des taux d’intérêt souvent compris entre 2 et 3 %, alors que les tarifs des crédits à la consommation sont largement supérieurs en règle générale. Selon la Banque de France, les crédits de moins de 3 000 € affichaient ainsi un taux moyen de 15,83 % au cours du 1er trimestre 2022. Les individus qui pratiquent ce genre d’arnaques sont aussi très bien organisés selon Thierry Pezennec, et « utilisent des call centers pour collecter, à la chaîne, des informations sur un maximum de particuliers et d’entreprises ». Derrière les adresses mail se cachent non pas une personne isolée, mais bel et un bien un véritable réseau qui cherche à toucher un maximum de personnes : « ﻿Nous avons affaire à de véritables organisations criminelles » explique le chef du Sirasco financier, qui, la plupart du temps, se situent en dehors de l’hexagone, dans des pays francophones tels que Madagascar ou au Maghreb. Pour éviter de se faire piéger, il faut donc rester très vigilant, ne transmettre aucune somme d’argent ou document à une personne inconnue et se méfier des propositions qui semblent trop alléchantes.
					</p>


